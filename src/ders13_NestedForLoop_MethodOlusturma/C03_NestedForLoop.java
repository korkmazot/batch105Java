package ders13_NestedForLoop_MethodOlusturma;

public class C03_NestedForLoop {

    public static void main(String[] args) {

        for (int i = 1; i <=4; i++) {

            for(int j = 1; j<=10; j++){

                System.out.print(i*j +  " ");
            }
            System.out.println("");
        }
    }

}
