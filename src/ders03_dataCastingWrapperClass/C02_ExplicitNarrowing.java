package ders03_dataCastingWrapperClass;

public class C02_ExplicitNarrowing {

    public static void main(String[] args) {

        int sayi = 20;

        short sh = (short)sayi;

        System.out.println(sayi);

        double dbl = 23.5;

        int say = (int)dbl;

        System.out.println(say);
    }
}
